public class Justin {
    private double altura;
    private String nombre;
    private double peso;

    public Justin(float altura, String nombre, float peso) {
        this.altura = altura;
        this.nombre = nombre;
        this.peso = peso;
    }

    public double getAltura() {
        return altura;
    }

    public String getNombre() {
        return nombre;
    }

    public double getPeso() {
        return peso;
    }
    
    
    
    
}
